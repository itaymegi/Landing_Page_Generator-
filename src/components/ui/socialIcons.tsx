import type { SocialLink, SocialPlatform } from '../../content/types'

export function inferSocialPlatform(link: SocialLink): SocialPlatform {
  if (link.icon) return link.icon
  const h = link.href.toLowerCase()
  if (h.includes('instagram')) return 'instagram'
  if (h.includes('facebook.com')) return 'facebook'
  if (h.includes('twitter.com') || h.includes('x.com')) return 'x'
  if (h.includes('youtube')) return 'youtube'
  if (h.includes('linkedin')) return 'linkedin'
  if (h.includes('tiktok')) return 'tiktok'
  if (h.includes('wa.me') || h.includes('whatsapp')) return 'whatsapp'
  if (h.includes('apps.apple.com')) return 'appStore'
  if (h.includes('play.google.com')) return 'googlePlay'
  return 'instagram'
}

